# Print Python conversion to tuple, list, set

s = 'geeks'
# Printing string conversion to tuple 
c = tuple(s)
print("After converting to tuple :", end="")
print(c)

# Printing string conversion to list
c = list(s)
print("After converting to list :", end="")
print(c)

# Printing string conversion to set
c = set(s)
print("After converting to set :", end="")
print(c)