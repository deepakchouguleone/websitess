# Python type conversion using ord, hex, oct
s = 'd'

# Printing character converting to integer
c = ord(s)
print("After converting to integer :", end="")
print(c)

# Printing character converting to hexadecimal
c = hex(ord(s))
print("After converting to hexadecimal :", end="")
print(c)

# Printing character converting to octal
c = oct(ord(s))
print("After converting to octal :", end="")
print(c)
